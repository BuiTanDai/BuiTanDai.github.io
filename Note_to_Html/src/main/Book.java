package main;

public class Book {

	String title;
	String author;
	String coverImage;
	String firstNote;
	String reference;
	
	public Book(String title, String author, String coverImage, String firstNote, String reference) {
		this.title = title;
		this.author = author;
		this.coverImage = coverImage;
		this.firstNote = firstNote;
		this.reference = reference;
	}

}
